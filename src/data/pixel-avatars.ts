/**
 * 在 CanvasRenderingContext2D 上绘制像素头像
 * @param ctx     2D 渲染上下文
 * @param id      Agent id
 * @param x       起始 x 坐标
 * @param y       起始 y 坐标
 * @param blockSize  每个像素块的尺寸（默认 4px，8×4=32px）
 * @param color      主色（hex）
 */
export function drawPixelAvatar(
  ctx: CanvasRenderingContext2D,
  id: string,
  x: number,
  y: number,
  blockSize: number,
  color: string,
): void {
  const grid = PIXEL_AVATARS[id];
  if (!grid) return;

  // 从主色派生强调色（亮化 40%）
  const accentColor = color + '99'; // 60% alpha version

  grid.forEach((row, ry) => {
    row.forEach((pixel, rx) => {
      if (!pixel) return;
      ctx.fillStyle = pixel === 2 ? accentColor : color;
      ctx.fillRect(x + rx * blockSize, y + ry * blockSize, blockSize - 1, blockSize - 1);
    });
  });
}
